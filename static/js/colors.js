ROOM3.getHexByColorName = function (n) {
    var c = ROOM3.getColorByName(n);
    var r = Math.floor(255 * c.r).toString(16);
    var g = Math.floor(255 * c.g).toString(16);
    var b = Math.floor(255 * c.b).toString(16);
    r = (r.length === 1) ? '0' + r : r;
    g = (g.length === 1) ? '0' + g : g;
    b = (b.length === 1) ? '0' + b : b;
    return '#' + r + g + b;
};
